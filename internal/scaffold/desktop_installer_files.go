// writeDesktopInstallerFiles drops the Windows NSIS installer templates,
// the WebView2 staging directory, and the release-desktop.sh pipeline
// into the desktop scaffold. Two installers ship by default:
//
//   - project.nsi          — full installer (~150 MB) that bundles the
//                            Microsoft Edge WebView2 Evergreen Standalone
//                            Installer so it works on machines with no
//                            internet / no admin during first launch.
//   - project-slim.nsi     — slim installer (~22 MB) using the online
//                            Evergreen bootstrapper.
//
// The release pipeline (scripts/release-desktop.sh) builds the binary
// once and runs makensis twice with different -D flags to produce both.
func writeDesktopInstallerFiles(root string, opts DesktopOptions) error {
	files := map[string]string{
		filepath.Join(root, "build", "windows", "installer", "project.nsi"):         desktopNSIFull(opts),
		filepath.Join(root, "build", "windows", "installer", "project-slim.nsi"):    desktopNSISlim(opts),
		filepath.Join(root, "build", "windows", "installer", ".gitignore"):          desktopInstallerGitignore(),
		filepath.Join(root, "build", "windows", "installer", "README.md"):           desktopInstallerReadme(opts),
		filepath.Join(root, "build", "windows", "installer", "webview2", ".gitkeep"): "",
		filepath.Join(root, "scripts", "release-desktop.sh"):                        desktopReleaseScript(opts),
	}

	for path, content := range files {
		if err := writeFile(path, content); err != nil {
			return fmt.Errorf("writing %s: %w", path, err)
		}
	}
	return nil
}

/* ─── NSIS: full installer ────────────────────────────────────────── */

func desktopNSIFull(opts DesktopOptions) string {
	tpl := `####
## <PROJECT> NSIS installer — full variant
##
## Flow:
##   1. Welcome page — title + version + "Next"
##   2. Directory page — operator sees + can override the install path
##   3. Install page — file-extraction progress
##   4. Finish page — "Launch <PROJECT>" checkbox + "Finish"
##
## Single-instance: a named mutex blocks a second copy of the installer
## from running in parallel.
##
## Install location defaults to %LOCALAPPDATA%\Programs\<PROJECT>
## (the Slack / Discord / VSCode / GitHub Desktop convention) — no
## admin required, the in-app auto-updater can swap the binary
## without UAC.
##
## Installed binary is named <PROJECT>.exe (no version suffix) so
## os.Executable() in the auto-updater always resolves to the same
## canonical path regardless of which version is running.
####
Unicode true

# Overrides applied BEFORE wails_tools.nsh so it doesn't re-set them
# from the wails.json defaults.
!define PRODUCT_EXECUTABLE "<PROJECT>.exe"
!define UNINST_KEY_NAME    "<PROJECT>"

!include "wails_tools.nsh"

VIProductVersion "${INFO_PRODUCTVERSION}.0"
VIFileVersion    "${INFO_PRODUCTVERSION}.0"

VIAddVersionKey "CompanyName"     "${INFO_COMPANYNAME}"
VIAddVersionKey "FileDescription" "${INFO_PRODUCTNAME} Installer"
VIAddVersionKey "ProductVersion"  "${INFO_PRODUCTVERSION}"
VIAddVersionKey "FileVersion"     "${INFO_PRODUCTVERSION}"
VIAddVersionKey "LegalCopyright"  "${INFO_COPYRIGHT}"
VIAddVersionKey "ProductName"     "${INFO_PRODUCTNAME}"

ManifestDPIAware true

!include "MUI.nsh"

!define MUI_ICON   "..\icon.ico"
!define MUI_UNICON "..\icon.ico"
!define MUI_FINISHPAGE_NOAUTOCLOSE
!define MUI_ABORTWARNING

# Branded MUI bitmaps — generated by scripts/release-desktop.sh from
# icon.ico if they don't exist. header.bmp (150x57) sits upper-right
# of every page; welcome.bmp (164x314) is the Welcome+Finish banner.
!define MUI_HEADERIMAGE
!define MUI_HEADERIMAGE_BITMAP            "header.bmp"
!define MUI_HEADERIMAGE_UNBITMAP          "header.bmp"
!define MUI_HEADERIMAGE_RIGHT
!define MUI_WELCOMEFINISHPAGE_BITMAP      "welcome.bmp"
!define MUI_UNWELCOMEFINISHPAGE_BITMAP    "welcome.bmp"

# Welcome page text overrides — these MUST be defined before
# MUI_PAGE_WELCOME is inserted.
!define MUI_WELCOMEPAGE_TITLE   "Welcome to the <PROJECT> Setup Wizard"
!define MUI_WELCOMEPAGE_TEXT    "This will install <PROJECT> v${INFO_PRODUCTVERSION} on your computer.$\r$\n$\r$\nClick Next to continue, or Cancel to exit the wizard."

# Directory page — surface the install location front and centre.
!define MUI_DIRECTORYPAGE_TEXT_TOP "Setup will install <PROJECT> in the folder below. To install in a different folder, click Browse and select another folder. Click Install to start the installation."
!define MUI_DIRECTORYPAGE_TEXT_DESTINATION "Install location"

# Finish page run-on-exit option.
!define MUI_FINISHPAGE_RUN      "$INSTDIR\${PRODUCT_EXECUTABLE}"
!define MUI_FINISHPAGE_RUN_TEXT "Launch <PROJECT> now"

!insertmacro MUI_PAGE_WELCOME
!insertmacro MUI_PAGE_DIRECTORY
!insertmacro MUI_PAGE_INSTFILES
!insertmacro MUI_PAGE_FINISH

!insertmacro MUI_UNPAGE_CONFIRM
!insertmacro MUI_UNPAGE_INSTFILES

!insertmacro MUI_LANGUAGE "English"

# Install WebView2 from the Microsoft Evergreen Standalone Installer
# (~125 MB) that we bundle inside the NSIS. Replaces wails.webview2runtime
# (the 1.8 MB online bootstrapper that requires internet to fetch the
# actual runtime at install time).
#
# The standalone installer file must be staged at
# build\windows\installer\webview2\MicrosoftEdgeWebView2RuntimeInstaller.exe
# before running wails build. scripts/release-desktop.sh downloads it
# once on demand and caches it across releases.
!macro grit.webview2offline
    # Bail if WebView2 is already installed system-wide.
    ReadRegStr $0 HKLM "SOFTWARE\WOW6432Node\Microsoft\EdgeUpdate\Clients\{F3017226-FE2A-4295-8BDF-00C3A9A7E4C5}" "pv"
    StrCmp $0 "" 0 webview2_already_installed
    ReadRegStr $0 HKLM "SOFTWARE\Microsoft\EdgeUpdate\Clients\{F3017226-FE2A-4295-8BDF-00C3A9A7E4C5}" "pv"
    StrCmp $0 "" 0 webview2_already_installed
    ReadRegStr $0 HKCU "Software\Microsoft\EdgeUpdate\Clients\{F3017226-FE2A-4295-8BDF-00C3A9A7E4C5}" "pv"
    StrCmp $0 "" 0 webview2_already_installed

    DetailPrint "Installing Microsoft Edge WebView2 Runtime (offline)..."
    CreateDirectory "$pluginsdir\wv2offline"
    SetOutPath "$pluginsdir\wv2offline"
    File "webview2\MicrosoftEdgeWebView2RuntimeInstaller.exe"
    ExecWait '"$pluginsdir\wv2offline\MicrosoftEdgeWebView2RuntimeInstaller.exe" /silent /install' $0
    DetailPrint "WebView2 install exited with code $0"
    Goto webview2_done

    webview2_already_installed:
        DetailPrint "WebView2 already installed — skipping."

    webview2_done:
!macroend

Function .onInit
    !insertmacro wails.checkArchitecture

    # Single-instance check — only the literal ERROR_ALREADY_EXISTS (183).
    # Using a per-user name (no "Global\") avoids false positives across
    # user sessions on the same Windows install.
    System::Call 'kernel32::CreateMutex(p 0, i 0, t "${INFO_PRODUCTNAME}_Installer") p .r0'
    System::Call 'kernel32::GetLastError() i .r1'
    IntCmp $1 183 0 +3 +3
        MessageBox MB_OK|MB_ICONEXCLAMATION "${INFO_PRODUCTNAME} installer is already running."
        Abort
FunctionEnd

Section
    !insertmacro wails.setShellContext

    # Install the offline WebView2 runtime bundled in this installer.
    !insertmacro grit.webview2offline

    !insertmacro wails.files

    CreateShortcut "$SMPROGRAMS\${INFO_PRODUCTNAME}.lnk" "$INSTDIR\${PRODUCT_EXECUTABLE}"
    CreateShortCut "$DESKTOP\${INFO_PRODUCTNAME}.lnk" "$INSTDIR\${PRODUCT_EXECUTABLE}"

    !insertmacro wails.writeUninstaller
SectionEnd

Section "uninstall"
    !insertmacro wails.setShellContext

    RMDir /r "$AppData\${PRODUCT_EXECUTABLE}"
    RMDir /r $INSTDIR
    Delete "$SMPROGRAMS\${INFO_PRODUCTNAME}.lnk"
    Delete "$DESKTOP\${INFO_PRODUCTNAME}.lnk"

    !insertmacro wails.deleteUninstaller
SectionEnd
`
	return strings.ReplaceAll(tpl, "<PROJECT>", opts.ProjectName)
}

