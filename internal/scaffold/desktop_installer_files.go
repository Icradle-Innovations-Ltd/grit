/* ─── README for the installer dir ────────────────────────────────── */

func desktopInstallerReadme(opts DesktopOptions) string {
	return fmt.Sprintf(`# Windows installer

Two NSIS templates ship in this directory:

| File | Output | Size | When to use |
|---|---|---|---|
| project.nsi | %s-Setup-v%s.exe | ~150 MB | Bundles the WebView2 runtime — works offline / behind restrictive firewalls. |
| project-slim.nsi | %s-Setup-Slim-v%s.exe | ~22 MB | Downloads WebView2 at install time. Smaller, but needs internet. |

Both are produced by ` + "`scripts/release-desktop.sh`" + `. You should not need to invoke ` + "`makensis`" + ` by hand.

## Branded look (header.bmp + welcome.bmp)

The release script regenerates both bitmaps from ` + "`../icon.ico`" + ` every build — no design work required. Override by replacing this directory's ` + "`icon.ico`" + `; the bitmaps fall in line on the next release.

## WebView2 offline runtime

The full installer bundles ` + "`webview2/MicrosoftEdgeWebView2RuntimeInstaller.exe`" + ` (~125 MB, the Microsoft Edge WebView2 Evergreen Standalone). The release script downloads it once and caches it; you don't commit the binary.

## Code signing (recommended, optional)

A signed .exe skips Windows SmartScreen. A code-signing cert is ~$200/year and pays for itself the first time an enterprise customer doesn't open a support ticket. See ` + "`/docs/desktop/auto-update#code-signing`" + ` for the signtool.exe wiring.
`, opts.ProjectName, "0.1.0", opts.ProjectName, "0.1.0")
}

/* ─── Release script ──────────────────────────────────────────────── */

func desktopReleaseScript(opts DesktopOptions) string {
	tpl := `#!/usr/bin/env bash
# scripts/release-desktop.sh — one-shot release pipeline.
#
# Usage:
#     scripts/release-desktop.sh 1.2.3
#
# What it does:
#     1. Bumps wails.json's productVersion + outputfilename
#     2. Bumps version.go's AppVersion constant
#     3. (Re)generates branded NSIS bitmaps from icon.ico
#     4. Downloads + caches the offline WebView2 runtime
#     5. Runs 'wails build -nsis' (produces raw .exe + full installer)
#     6. Runs makensis on project-slim.nsi (produces slim installer)
#     7. Tags vX.Y.Z, pushes the tag
#     8. 'gh release create' uploads all four assets
#
# Expects to be run from project root.
# Requires: bash, jq, wails, makensis, gh, powershell (Windows).

set -euo pipefail

if [ "$#" -ne 1 ]; then
    echo "Usage: $0 <version>     # e.g. $0 1.2.3"
    exit 1
fi

VERSION="$1"
PROJECT="<PROJECT>"
TAG="v${VERSION}"

echo ">> Releasing ${PROJECT} ${TAG}"

# ─── 1. Bump wails.json ──────────────────────────────────────────────
echo ">> Bumping wails.json productVersion + outputfilename"
python3 - <<PYBUMP
import json
with open("wails.json", "r", encoding="utf-8") as f:
    data = json.load(f)
data["info"]["productVersion"] = "${VERSION}"
data["outputfilename"] = "${PROJECT}-v${VERSION}"
with open("wails.json", "w", encoding="utf-8") as f:
    json.dump(data, f, indent=2)
    f.write("\n")
PYBUMP

# ─── 2. Bump version.go ──────────────────────────────────────────────
echo ">> Bumping version.go AppVersion"
sed -i.bak -E 's/(AppVersion = ")[^"]+(")/\1'"${VERSION}"'\2/' version.go
rm -f version.go.bak

# ─── 3. Generate branded bitmaps from icon.ico ───────────────────────
echo ">> Generating NSIS bitmaps from icon.ico"
ICON="build/windows/icon.ico"
INSTDIR="build/windows/installer"
if [ ! -f "${ICON}" ]; then
    echo "!! ${ICON} missing — wails generates it on first build. Run 'wails build' once."
    exit 1
fi
powershell.exe -NoProfile -ExecutionPolicy Bypass -Command "
Add-Type -AssemblyName System.Drawing
function Save-Bmp(\$icon, \$w, \$h, \$out) {
    \$bmp = New-Object System.Drawing.Bitmap \$w, \$h
    \$g = [System.Drawing.Graphics]::FromImage(\$bmp)
    \$g.Clear([System.Drawing.Color]::White)
    \$src = [System.Drawing.Icon]::ExtractAssociatedIcon('${ICON}')
    \$g.DrawIcon(\$src, [int]((\$w - \$src.Width) / 2), [int]((\$h - \$src.Height) / 2))
    \$bmp.Save(\$out, [System.Drawing.Imaging.ImageFormat]::Bmp)
    \$g.Dispose()
    \$bmp.Dispose()
}
Save-Bmp '${ICON}' 150 57 '${INSTDIR}/header.bmp'
Save-Bmp '${ICON}' 164 314 '${INSTDIR}/welcome.bmp'
"

# ─── 4. Cache WebView2 offline runtime ───────────────────────────────
WEBVIEW2_DIR="${INSTDIR}/webview2"
WEBVIEW2_BIN="${WEBVIEW2_DIR}/MicrosoftEdgeWebView2RuntimeInstaller.exe"
mkdir -p "${WEBVIEW2_DIR}"
if [ ! -f "${WEBVIEW2_BIN}" ]; then
    echo ">> Downloading WebView2 offline runtime (~125 MB) — one-time"
    # Microsoft's Evergreen Standalone Installer (x64). Stable URL.
    curl -L "https://go.microsoft.com/fwlink/?linkid=2124701" \
        -o "${WEBVIEW2_BIN}.tmp"
    mv "${WEBVIEW2_BIN}.tmp" "${WEBVIEW2_BIN}"
fi

# ─── 5. wails build -nsis (raw .exe + full installer) ────────────────
echo ">> wails build -nsis -platform windows/amd64"
wails build -nsis -platform windows/amd64 -trimpath -ldflags '-s -w'

BIN_DIR="build/bin"
RAW_EXE="${BIN_DIR}/${PROJECT}-v${VERSION}.exe"
FULL_SETUP="${BIN_DIR}/${PROJECT}-v${VERSION}-amd64-installer.exe"
TARGET_FULL="${BIN_DIR}/${PROJECT}-Setup-v${VERSION}.exe"
if [ -f "${FULL_SETUP}" ]; then
    mv "${FULL_SETUP}" "${TARGET_FULL}"
fi

# ─── 6. Slim installer ───────────────────────────────────────────────
echo ">> makensis: slim installer"
SLIM_TARGET="${BIN_DIR}/${PROJECT}-Setup-Slim-v${VERSION}.exe"
makensis \
    "-DARG_WAILS_AMD64_BINARY=$(pwd)/${RAW_EXE}" \
    "-DARG_OUT_FILE=$(pwd)/${SLIM_TARGET}" \
    "${INSTDIR}/project-slim.nsi"

# ─── 7. Tag + push ───────────────────────────────────────────────────
echo ">> git tag ${TAG}"
git add wails.json version.go
git commit -m "chore: release ${TAG}" || echo "nothing to commit"
git tag -a "${TAG}" -m "Release ${TAG}"
git push origin main
git push origin "${TAG}"

# ─── 8. GitHub release ───────────────────────────────────────────────
echo ">> gh release create ${TAG}"
gh release create "${TAG}" \
    --title "${TAG}" \
    --generate-notes \
    "${RAW_EXE}" \
    "${TARGET_FULL}" \
    "${SLIM_TARGET}"

echo ">> Done — ${TAG} published with raw .exe + full installer + slim installer."
`
	return strings.ReplaceAll(tpl, "<PROJECT>", opts.ProjectName)
}
